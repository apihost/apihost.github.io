(( EUID == 0 )) && { echo >&2 'This script should not be run as root!'; exit 1; }

# -------------------------------------------------------------------------------------------------------------------- #
# -----------------------------------------------------< SCRIPT >----------------------------------------------------- #
# -------------------------------------------------------------------------------------------------------------------- #

function yamlJson() {
  for i in *.yml; do
    yq -p 'yaml' -o 'json' -I 0 "${i}" > "${i%.*}.json"
  done
}

function main() {
  yamlJson
}; main "$@"
